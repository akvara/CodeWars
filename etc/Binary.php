function decodeBin($in) {
    foreach (explode(' ', $in) as $bi) {
        echo chr(bindec($bi));
    }
    echo PHP_EOL;
}

function encodeBin($out) {
    for ($i=0; $i<strlen($out); $i++) {
        echo decbin(ord($out[$i])) . " ";
    }
    echo PHP_EOL;
}

function decodeOct($in) {
    foreach (explode(' ', $in) as $bi) {
        echo chr(octdec($bi));
    }
    echo PHP_EOL;
}


