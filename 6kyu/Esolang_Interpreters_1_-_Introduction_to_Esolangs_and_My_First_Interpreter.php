use PHPUnit\Framework\TestCase;

function my_first_interpreter(string $code): string {
    $cell = 0;
    $out = '';
    for ($i = 0; $i < strlen($code); $i++) {
        switch ($code[$i]) {
            case '+':
                $cell++;
                if ($cell > 255) $cell = 0;
                break;
            case '.':
                $out .= chr($cell);
                break;
        }
    }
    return $out;
}


